package javacore.chapter01.variable.exercice;

public class CelsiusFahrenheitConverter {

    public static void main(String[] args) {

        double temperatureEnCelsius = 30.0;
        // Appliquer la formule de conversion Celsius vers Fahrenheit
        double temperatureEnFahrenheit = (temperatureEnCelsius * 1.8) + 32;
        // Afficher le résultat
        System.out.println(temperatureEnCelsius + "°C équivaut à " + temperatureEnFahrenheit + "°F");




    }
}






